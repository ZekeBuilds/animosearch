export const getFeaturedDestinations = (count = 6) =>
  destinations.sort((a, b) => b.rating - a.rating).slice(0, count)

export const getDestinationBySlug = (slug) =>
  destinations.find(d => d.slug === slug)

export const getRelatedDestinations = (slug, count = 4) => {
  const current = getDestinationBySlug(slug)
  if (!current) return []
  return destinations
    .filter(d => d.slug !== slug && (
      d.island_group === current.island_group ||
      d.activities.some(a => current.activities.includes(a))
    ))
    .slice(0, count)
}
