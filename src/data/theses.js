// Helper: get featured theses
export const getFeaturedTheses = (limit = 6) => theses.filter(t => t.featured).slice(0, limit)

// Helper: get thesis by slug
export const getThesisBySlug = (slug) => theses.find(t => t.slug === slug) || null

// Helper: get theses by college
export const getThesesByCollege = (collegeId) => theses.filter(t => t.college === collegeId.toUpperCase())

// Helper: get related theses (same college, different thesis)
export const getRelatedTheses = (slug, limit = 4) => {
  const thesis = getThesisBySlug(slug)
  if (!thesis) return []
  return theses
    .filter(t => t.slug !== slug && t.college === thesis.college)
    .slice(0, limit)
}

